# 5. Program to perform area of circle. 

# Taking radius of the circle as input

radius = float(input("Enter the radius of the circle : "))

# Performing the area of circle operation

area = 3.14 * radius * radius # Area of circle = πr²

# Printing the result

print("The area of the circle is : ",area)

